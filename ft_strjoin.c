#include "libft.h"
#include <stdlib.h>

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*ptr;
	int		i;

	if (!s1 || !s2)
		return (NULL);
	i = 0;
	if (!(ptr = malloc(sizeof(char) * (ft_strlen((char *)s1)
						+ ft_strlen((char *)s2) + 1))))
		return (NULL);
	while (*s1)
		ptr[i++] = *s1++;
	while (*s2)
		ptr[i++] = *s2++;
	ptr[i] = '\0';
	return (ptr);
}
